int		ft_atoi(char *str)
{
	int nb;
	int neg;
	int valnb1;

	neg = 0;
	if (str[0] == '-')
	{
		neg = 1;
	}
	nb = 0;
	while (*str != 0)
	{
		valnb1 = *str - '0';
		nb = nb * 10;
		nb = nb + valnb1;
		str++;
	}
	if (neg == 1)
	{
		valnb1 = valnb1 * (-1);
	}
	return (nb);
}

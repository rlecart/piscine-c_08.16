void	doline(int x, char l[], char *str, int *i)
{
	if (x >= 1)
	{
		str[*i] = l[0];
		(*i)++;
	}
	while (x - 2 > 0)
	{
		str[*i] = l[1];
		(*i)++;
		x--;
	}
	if (x >= 2)
	{
		str[*i] = l[2];
		(*i)++;
	}
	if (x >= 1)
	{
		str[*i] = '\n';
		(*i)++;
	}
}
